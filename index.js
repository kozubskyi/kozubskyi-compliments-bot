        creatorChatId,
        `–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å '${firstName} ${lastName} <${username}> (${chatId})' –æ—Ç–ø—Ä–∞–≤–∏–ª(-–∞) —Å–æ–æ–±—â–µ–Ω–∏–µ '${command}' –∏ –ø–æ–ª—É—á–∏–ª(-–∞) –æ—Ç–≤–µ—Ç '${response}'`
